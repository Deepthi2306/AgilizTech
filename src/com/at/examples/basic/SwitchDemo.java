package com.at.examples.basic;

public class SwitchDemo {

	public static void main(String[] args) {
		for(int i=0;i<=8;i++) {
			switch(i) {
			case 0: System.out.println("i value here is " +i);
					break;
			case 1: System.out.println("i value here is " +i);
					break;
			case 2: System.out.println("i value here is " +i);
					break;
			case 3: System.out.println("i value here is " +i);
					break;
			case 4: System.out.println("i value here is " +i);
					break;
			case 5: System.out.println("i value here is " +i);
					break;
			default: System.out.println("i value other than 5");
			}
		}

	}

}
